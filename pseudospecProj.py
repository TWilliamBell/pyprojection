import projection as proj
import pseudopy as ps
import numpy as np
from matplotlib import pyplot

def pseudospecProj(A, phi, eps = 1e-10, real_min = -50, real_max = 50,
                   real_n = 101, imag_min = -25, imag_max = 25,
                   imag_n = 101, pushaway = 100):
    P, commutatorNorm, commutator = proj.projectionCommute(A, phi, eps)
    IP = np.identity(A.shape[0])-P
    PA = np.matmul(P, A)-100*np.abs(real_min)*pushaway*IP
    pseudo = ps.NonnormalMeshgrid(PA, real_min = real_min,
                    real_max = real_max, real_n = real_n,
                    imag_min = imag_min, imag_max = imag_max,
                    imag_n = imag_n)
    return pseudo, P

def defaultPlotPseudospec(pseudoMeshgrid, real_min = -50, real_max = 50,
                          imag_min = -25, imag_max = 25):
    pyplot.imshow(pseudoMeshgrid.Vals,
                  extent = (real_min, real_max, imag_min, imag_max))
    pyplot.colorbar()
    pyplot.xlabel("Real Part")
    pyplot.ylabel("Imaginary Part")
    pyplot.show()

